# Positive Zahl
x = 12 / 7
print("12 / 7 =", x)

# Rundung und Konvertierung
rx = round(x, 3)
print("gerundet auf drei Stellen: ", rx)
rx = round(x)
print("gerundet auf null Stellen: ", rx)
ix = int(x)
print("gerundet mit int(x):", ix)

print()

# Negative Zahl
x = -12 / 7
print("-12 / 7 =", x)

# Rundung und Konvertierung
rx = round(x, 3)
print("gerundet auf drei Stellen: ", rx)
rx = round(x)
print("gerundet auf null Stellen: ", rx)
ix = int(x)
print("gerundet mit int(x):", ix)
